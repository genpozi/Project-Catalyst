
import { PluginDefinition, ProjectData } from '../types';

// Helper to generate Helm Charts
const generateHelmChart = (project: ProjectData) => {
    const appName = project.name.toLowerCase().replace(/[^a-z0-9]/g, '-');
    
    const chartYaml = `apiVersion: v2
name: ${appName}
description: A Helm chart for ${project.name}
type: application
version: 0.1.0
appVersion: "1.0.0"
`;

    const valuesYaml = `replicaCount: 2

image:
  repository: ${appName}
  pullPolicy: IfNotPresent
  tag: "latest"

service:
  type: ClusterIP
  port: 80

ingress:
  enabled: true
  className: "nginx"
  hosts:
    - host: ${appName}.local
      paths:
        - path: /
          pathType: ImplementationSpecific
`;

    const deploymentYaml = `apiVersion: apps/v1
kind: Deployment
metadata:
  name: {{ include "${appName}.fullname" . }}
  labels:
    {{- include "${appName}.labels" . | nindent 4 }}
spec:
  replicas: {{ .Values.replicaCount }}
  selector:
    matchLabels:
      {{- include "${appName}.selectorLabels" . | nindent 6 }}
  template:
    metadata:
      labels:
        {{- include "${appName}.selectorLabels" . | nindent 8 }}
    spec:
      containers:
        - name: {{ .Chart.Name }}
          image: "{{ .Values.image.repository }}:{{ .Values.image.tag }}"
          ports:
            - name: http
              containerPort: 80
`;

    return [
        { filename: `helm/${appName}/Chart.yaml`, content: chartYaml },
        { filename: `helm/${appName}/values.yaml`, content: valuesYaml },
        { filename: `helm/${appName}/templates/deployment.yaml`, content: deploymentYaml }
    ];
};

