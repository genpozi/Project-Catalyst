const generateVscodeSettings = (project: ProjectData) => {
    const settings = {
        "editor.formatOnSave": true,
        "editor.defaultFormatter": "esbenp.prettier-vscode",
        "files.exclude": {
            "**/.git": true,
            "**/.DS_Store": true
        },
        "search.exclude": {
            "**/node_modules": true,
            "**/dist": true
        }
    };
    
    const extensions = {
        "recommendations": [
            "esbenp.prettier-vscode",
            "dbaeumer.vscode-eslint"
        ]
    };

    return [
        { filename: '.vscode/settings.json', content: JSON.stringify(settings, null, 2) },
        { filename: '.vscode/extensions.json', content: JSON.stringify(extensions, null, 2) }
    ];
};

